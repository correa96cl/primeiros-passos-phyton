#Inteiro - Integer
numero_inteiro = 3
print("inteiro = ", numero_inteiro)
# Real com ponto flutuante - Float
numero_real = 3.14
print("Real", numero_real)


#type() devolver o tipo da variavel

print("Tipo da variavel inteiro = ", type(numero_real))

#soma
soma = 1 + 1
print("1 + 1 = ", soma)


#subtracao =
subtracao = 1-1
print("1 - 1 = ", subtracao)

#multiplicacao
multiplicacao = 1*1
print("1 * 1 = ", multiplicacao)


#divisao
divisao = 5 / 2
print("Divisao ", divisao)
print("Tipo da variavel do resultado da divisao = ", type(divisao))
print("Valor em inteiro = ", int(divisao))

#float
print("Valor em float = ", float(soma))

#Modulo %
modulo = 5 % 2
print("Modulo ", modulo)


#divisao inteiro
divisao = 5 // 2
print("Divisao ", divisao)
print("Tipo da variavel do resultado da divisao = ", type(divisao))

